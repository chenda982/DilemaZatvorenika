    public void napuniMatricu ()
    {
       
        R = Integer.parseInt(jTextField10.getText());
        T = Integer.parseInt(jTextField11.getText());
        S = Integer.parseInt(jTextField12.getText());
        P = Integer.parseInt(jTextField13.getText());
        r = Integer.parseInt(jTextField3.getText());
        t = Integer.parseInt(jTextField4.getText());
        s = Integer.parseInt(jTextField5.getText());
        p = Integer.parseInt(jTextField6.getText());
        proveriParm(r, t, s, p);
        proveriParm(R, T, S, P);
        
    }
    public void proveriParm(int r, int t, int s, int p)
    {
        if (r>t)
          throw new IllegalArgumentException();
        if (p>r)
          throw new IllegalArgumentException();
        if (s>p)
          throw new IllegalArgumentException();
    }
